class a2q1
{
        public static void main(String arg[])
        {
                int a,b,i,j;
                a = Integer.parseInt(arg[0]);
                b = Integer.parseInt(arg[1]);
                for(i=1;i<=10;i++)
		{
			for(j=a;j<=b;j++)
			{	
				System.out.print(+j+"*"+i+"="+(j*i)+"\t");
			}
			System.out.print("\n");
                }
        }
};


